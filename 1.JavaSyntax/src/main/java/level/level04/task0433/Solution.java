package level.level04.task0433;


/* 
Гадание на долларовый счет
*/

public class Solution {
    public static void main(String[] args) throws Exception {
        int k = 0, i;
        final int ten = 10;
        while(k++ < ten){
            i = 0;
            while(i++ < ten)
                System.out.print('S');
            System.out.println();
        }
    }
}
