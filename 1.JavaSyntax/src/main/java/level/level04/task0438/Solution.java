package level.level04.task0438;

/* 
Рисуем линии
*/

public class Solution {
    public static void main(String[] args) throws Exception {
        final int ten = 10;
        for (int i = 0; i < ten; i++) {
            System.out.print(8);
        }
        System.out.println();
        for (int i = 0; i < ten; i++) {
            System.out.println(8);
        }
    }
}
